    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $permission_name_before)
    {        
        Auth::user()->syncPermissions([$permission_name_before, $request->get('permission_name')]);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($permission_name)
    {
        Auth::user()->revokePermissionTo($permission_name);
    }
